# 2、标记清楚
# 循环引用=>导致内存泄露问题
l1 = [111,]
l2 = [222,]
l1.append(l2) # l1=[值111的内存地址，l2列表的内存地址]
l2.append(l1) # l2=[值222的内存地址，l1列表的内存地址]
print(id(l1[1]),id(l2))
print(id(l1[2]),id(l1))

del l1
del l2
